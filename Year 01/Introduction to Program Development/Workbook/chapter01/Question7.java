/*******************************************************************************
 * Question  1.7 (page 81)   
 * Write an application that prints the outline of a tree using asterisk (*)
 * characters.
 ******************************************************************************/

public class Question7 {
    public static void main(String[] args) {
        System.out.println("        *        ");
        System.out.println("       * *       ");
        System.out.println("      *   *      ");
        System.out.println("     *     *     ");
        System.out.println("    *       *    ");
        System.out.println("   *         *   ");
        System.out.println("  *************  ");
        System.out.println("       *  *      ");
        System.out.println("      *    *     ");
        System.out.println("      *    *     ");
        System.out.println("      ******     ");
}
}
